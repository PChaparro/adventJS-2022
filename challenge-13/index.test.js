const { solve } = require('./index');

test('Test challenge #', () => {
  expect(
    solve(1546300800, [
      [3, 1546301100],
      [2, 1546300800],
      [1, 1546300800],
      [1, 1546300900],
      [1, 1546301000],
    ]),
  ).toEqual([1, 3]);

  expect(
    solve(1546300600, [
      [1, 1546300800],
      [2, 1546300800],
      [1, 1546300900],
      [1, 1546301000],
      [3, 1546301100],
    ]),
  ).toEqual([1, 2, 3]);

  expect(
    solve(1556300600, [
      [1, 1546300800],
      [2, 1546300800],
      [1, 1546300900],
      [1, 1546301000],
      [3, 1546301100],
    ]),
  ).toEqual([]);

  expect(solve(1556300600, [])).toEqual([]);
});
